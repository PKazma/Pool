int		ft_iterative_factorial(int nb)
{
	int i;

	i = 1;
	if (nb > 12 || nb < 0)
		return (0);
	else
	{
		while (nb > 0)
		{
			i *= nb;
			nb--;
		}
	}
	return (i);
}
